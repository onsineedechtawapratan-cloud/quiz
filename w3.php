<?php
// ================================
// กำหนดข้อมูลผู้ใช้ (แก้ไขได้)
// ================================

$username = "สมชาย";     // ชื่อผู้ใช้
$birthYear = 2450;       // ปีเกิด (พ.ศ.)
$currentYear = 2569;     // ปีปัจจุบัน

// ================================
// คำนวณอายุ
// ================================

$age = $currentYear - $birthYear;

// ================================
// แสดงข้อมูลผู้ใช้
// ================================

echo "<h2>ระบบตรวจสอบสิทธิ์เข้าใช้งานเว็บไซต์</h2>";
echo "ชื่อผู้ใช้: " . $username . "<br>";
echo "ปีเกิด: " . $birthYear . "<br>";
echo "<hr>";

// ================================
// ตรวจสอบความถูกต้องของข้อมูล
// ================================

// TODO:
// 1) ถ้าปีเกิดมากกว่าปีปัจจุบัน → แสดงข้อความผิดพลาด
// 2) ถ้าอายุมากกว่า 120 ปี → แสดงข้อความเตือน
// 3) ถ้าอายุน้อยกว่า 18 ปี → ไม่อนุญาตให้เข้าใช้งาน
// 4) ถ้าอายุ 18 ปีขึ้นไป → อนุญาตให้เข้าใช้งาน

if ( $birthYear > $currentYear ) {

    echo "ข้อมูลปีเกิดไม่ถูกต้อง";

} else if ( $age > 120 ) {

    echo "กรุณาตรวจสอบข้อมูลอีกครั้ง";

} else if ( $age < 18 ) {

    echo "อายุของคุณคือ " . $age . " ปี<br>";
    echo "สถานะ: ไม่อนุญาตให้เข้าใช้งานเว็บไซต์";

} else {

    echo "อายุของคุณคือ " . $age . " ปี<br>";
    echo "สถานะ: อนุญาตให้เข้าใช้งานเว็บไซต์";

}
?>